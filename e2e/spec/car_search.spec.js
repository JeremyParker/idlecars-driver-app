'use strict';

describe('Filtering cars', function () {
  var helpers = require('../spec_helper');
  var listing = require('../cars/list.po');
  var filterModal = require('../cars/filter_modal.po');

  beforeEach(function () {
    helpers.startTest();
    browser.get('http://localhost:3000/#/');
  });

  it('only shows cars matching the filter', function () {
    listing.filterButton.click();
    filterModal.luxToggle.click();
    expect(listing.carEls.count()).toBe(1);
  });
});
